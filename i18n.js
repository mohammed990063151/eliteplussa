function getLang() {
  const saved = localStorage.getItem('eliteplus-lang');
  if (saved === 'ar' || saved === 'en') return saved;
  return 'en';
}

function applyLanguage(lang) {
  const dict = translations[lang] || translations.en;
  const dir = lang === 'ar' ? 'rtl' : 'ltr';

  document.documentElement.lang = lang;
  document.documentElement.dir = dir;
  localStorage.setItem('eliteplus-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] != null) el.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria');
    if (dict[key] != null) el.setAttribute('aria-label', dict[key]);
  });

  document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
    const key = el.getAttribute('data-i18n-alt');
    if (dict[key] != null) el.setAttribute('alt', dict[key]);
  });

  const titleKey = document.body.getAttribute('data-title-key');
  if (titleKey && dict[titleKey]) {
    document.title = dict[titleKey];
  } else if (lang === 'ar') {
    document.title = 'إليت بلس — بوابة رأس مال متوافقة مع التوجه السيادي';
  } else {
    document.title = 'Elite Plus — Sovereign-Aligned Capital Gateway';
  }

  document.querySelectorAll('.lang-toggle button').forEach((btn) => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

function setLanguage(lang) {
  applyLanguage(lang);
}

document.addEventListener('DOMContentLoaded', () => {
  applyLanguage(getLang());
});
